/*
Continuare a chiedere all'utente un numero tra 1 e 10 fino a quando l'utente non inserisce il valore corretto
Math.floor((Math.random() * 9)+1)
 */

let number = 10;

let requestedNumber = parseInt(prompt('Inserire un numero'));


while (requestedNumber !== number){
    requestedNumber = parseInt(prompt('Inserire un numero'));
}
























